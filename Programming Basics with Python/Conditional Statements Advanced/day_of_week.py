current_day = int(input())

if current_day == 1:
    print("Monday")
elif current_day == 2:
    print("Tuesday")
elif current_day == 3:
    print("Wednesday")
elif current_day == 4:
    print("Thursday")
elif current_day == 5:
    print("Friday")
elif current_day == 6:
    print("Saturday")
elif current_day == 7:
    print("Sunday")
else:
    print("Error")
